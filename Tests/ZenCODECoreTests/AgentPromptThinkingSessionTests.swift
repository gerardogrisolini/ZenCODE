    }

    @Test
    func terminalSessionConfigurationUsesSessionIDCacheKeyByDefault() throws {
        let workingDirectory = URL(
            fileURLWithPath: "/tmp/ZenCODE-cache-project",
            isDirectory: true
        )
        let configuration = try AgentConfiguration(
            hostedModelID: "mlx-community/test",
            availableAgents: AgentProfileStore.defaultProfiles(),
            workingDirectory: workingDirectory
        )
        let terminal = TerminalChat(
            configuration: configuration,
            stdinIsTerminal: false
        )

        let sessionConfiguration = terminal.currentSessionConfiguration(
            allowedToolNames: []
        )

        #expect(sessionConfiguration.cacheKey == terminal.sessionID)
    }

    @Test
    func terminalSessionConfigurationPreservesLoadedCacheKey() throws {
        let workingDirectory = URL(
            fileURLWithPath: "/tmp/ZenCODE-cache-project",
            isDirectory: true
        )
        let configuration = try AgentConfiguration(
            hostedModelID: "mlx-community/test",
            availableAgents: AgentProfileStore.defaultProfiles(),
            workingDirectory: workingDirectory
        )
        let terminal = TerminalChat(
            configuration: configuration,
            stdinIsTerminal: false
        )
        terminal.activeSessionCacheKey = "terminal:/tmp/ZenCODE-cache-project:session:plan"

        let sessionConfiguration = terminal.currentSessionConfiguration(
            allowedToolNames: []
        )

        #expect(
            sessionConfiguration.cacheKey
                == "terminal:/tmp/ZenCODE-cache-project:session:plan"
        )
    }
}
